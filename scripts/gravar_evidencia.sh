echo -e "${CINZA}  $(date '+%H:%M:%S')${RESET}\n"

cd "$ROOT_DIR"
"$PYTHON" smoke_all.py --all 2>&1 \
  | sed -u "s|${N8N_WEBHOOK_URL}|https://***.***.n8n.cloud/webhook/hr-buddy|g" \
  | tee -a "$LOG_FILE" || true
EXIT_CODE=${PIPESTATUS[0]}

# в”Җв”Җ EvidГӘncias geradas в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
separador
echo ""
echo -e "${BOLD}  EvidГӘncias salvas em: relatorios/evidencias/${RESET}"
echo -e "${CINZA}  $(basename "$LOG_FILE")${RESET}"
[[ "$GRAVAR" == true && -f "$VIDEO_FILE" ]] && \
  echo -e "${CINZA}  $(basename "$VIDEO_FILE")${RESET}"
echo ""

[[ "$EXIT_CODE" -eq 0 ]] \
  && echo -e "${VERDE}${BOLD}  Todos os smokes passaram вҖ” evidГӘncia pronta!${RESET}\n" \
  || echo -e "${AMARELO}${BOLD}  Falhas detectadas вҖ” revise antes de publicar.${RESET}\n"

exit "$EXIT_CODE"
