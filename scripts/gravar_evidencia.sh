# в”Җв”Җ Carrega .env в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
[[ -f "$ROOT_DIR/.env" ]] && set -a && source "$ROOT_DIR/.env" && set +a

# в”Җв”Җ PrГ©-requisitos в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
echo ""
if [[ ! -x "$PYTHON" ]]; then
  erro "Virtualenv nГЈo encontrado em .venv/bin/python"
  erro "Crie com: python3 -m venv .venv && .venv/bin/pip install -r requirements.txt"
  exit 1
fi
ok "Python   вҶ’ $("$PYTHON" --version)"

if [[ -z "${N8N_WEBHOOK_URL:-}" ]]; then
  erro "N8N_WEBHOOK_URL nГЈo definida вҖ” configure no .env"
  exit 1
fi
ok "Webhook  вҶ’ ${N8N_WEBHOOK_URL:0:45}..."

if [[ -z "${GROQ_API_KEY:-}" ]]; then
  warn "GROQ_API_KEY nГЈo definida вҖ” juiz LLM desativado"
else
  ok "Groq     вҶ’ ${GROQ_API_KEY:0:12}..."
fi
separador

# в”Җв”Җ GravaГ§ГЈo de tela (opcional) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
FFMPEG_PID=""

parar_gravacao() {
  if [[ -n "$FFMPEG_PID" ]]; then
    kill -SIGINT "$FFMPEG_PID" 2>/dev/null
    wait "$FFMPEG_PID" 2>/dev/null || true
    FFMPEG_PID=""
  fi
}

trap parar_gravacao EXIT INT TERM

if [[ "$GRAVAR" == true ]]; then
  if ! command -v ffmpeg &>/dev/null; then
    warn "ffmpeg nГЈo encontrado вҖ” gravaГ§ГЈo de tela desativada"
    GRAVAR=false
  else
    DISPLAY_RES=$(xdpyinfo 2>/dev/null | awk '/dimensions/{print $2}' || echo "1920x1080")
    ffmpeg -y \
      -video_size "$DISPLAY_RES" -framerate 30 \
      -f x11grab -i "${DISPLAY:-:0}.0" \
      -c:v libx264 -preset fast -crf 20 \
      "$VIDEO_FILE" \
      >/dev/null 2>&1 &
    FFMPEG_PID=$!
    ok "Gravando вҶ’ $VIDEO_FILE"
    separador
  fi
fi

# в”Җв”Җ ExecuГ§ГЈo в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
echo ""
